class Solution:
    def longestValidParentheses(self, s: str) -> int:
        M = [[0 for i in range(len(s))] for j in range(len(s))]
        max_l = 0
        for i in range(0, len(s) - 1):
            if s[i] == "(" and s[i + 1] == ")":
                M[i][i + 1] = 2
                max_l = 2
        for r in range(4, len(s) + 1, 2):
            for i in range(len(s) - r + 1):
                j = i + r - 1
                if s[i] == "(" and s[j] == ")":
                    if M[i + 1][j - 1] != 0:
                        M[i][j] = M[i + 1][j - 1] + 2
                    else:
                        for k in range(i + 1, j - 1):
                            if s[k] == ")" and M[i][k] != 0 and M[k + 1][j] != 0:
                                M[i][j] = M[i][k] + M[k + 1][j]
                                break
                    max_l = max(max_l, M[i][j])
        print(max_l)
        return max_l

    def longestValidParentheses1(self, s: str) -> int:
        dp = [0 for i in range(len(s))]
        maxans = 0
        for i in range(1, len(s)):
            if s[i] == ")":
                if s[i - 1] == "(":
                    dp[i] = dp[i - 2] + 2
                elif i > dp[i - 1] and s[i - dp[i - 1] - 1] == "(":
                    dp[i] = (dp[i - dp[i - 1] - 2] if i - dp[i - 1] - 2 > 0 else 0) + dp[i - 1] + 2
                maxans = max(maxans, dp[i])
        print(maxans)
        return maxans


